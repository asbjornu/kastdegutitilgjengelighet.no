<h2>Bla igjennom tips om prinsipper for design</h2>

<p>Her er tipsene sortert etter prinsipper for design fremlagt av <acronym title="World Wide Web Consortium">W3C</acronym>. Din weblogg burde være:</p>

<ol>
<li><strong>Oppfattelig</strong>. Du burde forsikre seg om at alt innhold kan bli presentert på en slik måte at det er oppfattelig/forståelig for alle.</li>
<li><strong>Anvendelig</strong>. Du burde forsikre deg om at grensesnittets elementer i innholdet er anvendelig for alle.</li>
<li><strong>Navigerbar</strong>. Du burde forenkle orienteringen og navigasjonen av innholdet.</li>
<li><strong>Forståelig</strong>. Du burde gjøre det så enkelt som mulig for folk å forstå ditt innhold.</li>
<li><strong>Robust</strong>. Du burde bruke teknologi som maksimerer sjansen for at ditt innhold vil fungere med nåværende og fremtidige nettlesere,
spesielle teknologier, og andre programmer.</li>
</ol>

