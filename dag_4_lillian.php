<h2 class="entrysubject">Dag 4: Lillian</h2>
<p class="firstparagraph">Lillian er innvandrer til Norge -- fersk fra båten, som de sier (båten var egentlig et fly, og hun kom for 30 år siden, men de sier fortatt det) -- fra Hong Kong. Hun er 54 år gammel, enke, og bor i Kristiansand sammen med datteren sin, svigersønnen og det 2 år gamle barnebarnet. Til sammen tar de tre opp mesteparten av tiden hennes. Mens datteren og svigersønnen snakker to språk flytende, har Lillian fremdeles problemer med norsk, så hun snakker helst morsmålet sitt, kantonesisk. Når hun ikke leker med barnebarnet prøver hun å forbedre engelskferdighetene sine med å lese aviser. Hun bretter avisa ut over kjøkkenbordet, skrur på 100-watts-lampen og leser med forstørrelsesglass.</p>
<p>Lillian jobber som kontorassistent i et verdensomspennende telekommunikasjonskonglomerat. Tilfeldigvis akkurat det samme verdensomspennende telekommunikasjonskonglomeratet som er for inkompetent til å gi rike unge pensjonister i Grimstad høyhastighets internettilgang -- men dette vet ikke Lillian. IT-avdelingen deres har nettopp fullført en verdensomspennende overgang til Windows XP og Internet Explorer 6, og vil sannsynligvis være litt mer gretten enn vanlig de neste ni månedene. De har også innført nye sikkerhetsregler for internettbruk: ikke Java, ikke Javascript, ikke Flash, ingen ActiveX-kontroller, bortsett fra på nettsteder som er godkjente, og det er det ingen som er. Teknisk betyr dette at alle nettsteder er i begrenset sone i Internet Explorer sin sikkerhetsinnstilling, at alle skript i begrensede nettsteder er slått av og at man trenger administrativ tilgang for å legge til nettsteder i den klarerte sonen.</p>
<p>Lillian har selvsagt ikke administrativ tilgang.</p>
<p>Derimot har hun en 19-tommers skjerm, til tross for protester fra Matt i IT-avdelingen, som bruker enhver anledning til å minne henne på at han måtte bære den opp tre trapper fordi heisen var i ustand den dagen. Men han sier det på en fin måte. Lillian liker Matt; han er den greieste av dem, og har til og med satt tekststørrelsen i Lillians Internet Explorer til «Større» slik at teksten i webloggen til datteren hennes er leselig. Hun leser den hver dag. Men da hun spurte Matt hvorfor han ikke kunne gjøre skriften på <a href="http://www.cnn.com/">CNN.com</a> større, satte han i gang en av tiradene sine med en flom av nerdete, tekniske ord og ble veldig frustrert før han til slutt sa at det ikke var noe han kunne gjøre med det.</p>
<p>Lillian skulle ønske hun kunne lese flere nettsider, men hvis ikke Matt kan ordne det er det ingen som kan.</p>
