<h2 class="entrysubject">Dag 3: Knut</h2>
<p class="firstparagraph">Knut bor i en liten leilighet i Larvik, omkring én mil fra søsteren, som er hans eneste slektning i live. Han er 62 år gammel. Leiligheten hans er alltid strøken, som en militærkaserne like før inspeksjon. Dette er ganske naturlig, i og med at Knut har vært i militæret siden han var 18. Han startet som korporal, arbeidet seg oppover i gradene, ble hedret med tre medaljer i Libanon og forble i aktiv tjeneste helt til for ti år siden, da en kneskade tvang ham til skrivebordsarbeid. Så fikk han hjerneslag i fjor, og ble nødt til å gå av med pensjon. Han klarer ikke å røre på høyrearmen sin, og venstrearmen skjelver uten at noen har klart å finne grunnen. 500 personer kom til avskjedsfesten hans. Han kjente alle personlig.</p>
<p>Knut bruker en gammel bærbar datamaskin som kjører <a href="http://www.redhat.com/">Red Hat Linux</a>. Han surfer på nettet med <a href="http://www.mozilla.com/">Mozilla</a>, og leser e-post med <a href="http://ximian.com/products/ximian_evolution/">Evolution</a> -- men dette vet han knapt. Maskinen (og programvaren dens) var en pensjonsgave fra søsteren, som har skikkelig greie på disse datatingene. Knuts maskin er en av disse gamle bærbare som bare har én skjermoppløsning, 1024 x 768, selv om skjermen er på bare 13 tommer. Synet til Knut er helt fint, det, men iblant blir øynene forvirret og mister taket i det de leser. Dette startet etter slaget, og søsteren tror det blir verre med tiden -- men hun tar åpenbart feil.</p>
<p>Det eneste datautstyret han har spandert på seg er et ekstra tastatur med piltaster og PageUp/PageDown-taster, slik at han lettere når disse med den friske armen. 199 kroner minus en rabatt på 30 kroner.</p>
<p>Knut var aldri på nettet før han pensjonerte seg, men nå finner han stadig flere grunner til å bli der. Fire timer om dagen sitter han og leser nettsider og snakker med nye og gamle venner. Oppmuntret av veterankameratene sine og med teknisk assistanse fra søsteren har han startet et e-post-nyhetsbrev om rettigheter for krigsveteraner. Det har allerede 200 medlemmer.</p>
<p>Knut kan skrive så mye som 10 ord i minuttet (neste år blir det 15) ved å støtte venstrehånden mot tastaturkanten og bare bevege fingrene. Musa kan han imidlertid omtrent ikke bruke, og han flytter seg rundt med piltastene, tabulatortasten og en mengde hurtigtaster søsteren har vist ham. Hun har også lært ham hvordan han kan bruke et spesielt program til å flytte musepekeren med piltastene, men dette går tregt og det blir fort feil, så han gjør det sjelden.</p>
<p>Selv om Knut ennå ikke vet det, kommer nyhetsbrevet hans til å bli til en weblogg neste år. Året etter det vil weblogen bli til en politisk aksjonskomité med stor politisk tyngde. Når alt kommer til alt handler politikk bare om hvem du kjenner, og Knut kjenner alle.</p>
