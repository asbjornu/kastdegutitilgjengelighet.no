<h2 class="entrysubject">Dag 5: Marius</h2>
<p class="firstparagraph">Marius er sønn av en narkotikamisbruker og har vært blind siden fødselen. Han ble født tre måneder for tidlig, lå i respirator i seks uker og fikk 10% sjanse for å overleve. Han er 31 år gammel.</p>
<p>Marius jobber ved en av AT+Ts telefonsentraler, der han overfører samtaler mellom døve og hørende. Det fungerer slik:</p>
<ol>
<li>En døv eller hørselshemmet person, la oss kalle henne Melissa, ringer til sentralen med en TDD eller datamaskin.</li>
<li>Melissa skriver navnet sitt og nummeret til personen hun vil ringe til, vi kaller ham Todd.</li>
<li>Marius ringer til Todd og forteller ham at Melissa ringer ham via telefonsentralen.</li>
<li>Marius leser det Melissa skriver ved hjelp av en <a href="http://www.alvabraille.com/BrailleDisplays/default.asp">ALVA Braille-viser</a>, som konverterer teksten til Braille i sanntid.</li>
<li>Marius skriver det Todd sier til Melissa med en hastighet på hele 110 ord i minuttet.</li>
</ol>
<p>Hverken Melissa eller Todd vet eller bryr seg om at Marius er blind. Den eneste indikasjonen på dette er lyden av ALVA-pinnene som spretter opp og ned, omtrent som klikkelydene av en gammeldags skrivemaskin i det fjerne.</p>
<p>Det finnes ingen kobling mellom øynene og hjernen til Marius. Likevel klager han over hodepine når solen skinner inn gjennom vinduet nær kontorbåsen hans, og han trekker for gardinene hver ettermiddag.</p>
<p>Marius bruker en ALVA hjemme også, sammen med den tekstbaserte nettleseren <a href="http://www.fdisk.com/doslynx/lynxport.htm">Lynx</a> i et fullskjerms DOS-vindu. Han leser nettsider på samme måte som han leser telefonsamtaler på jobben: i Braille, én linje om gangen. Han hater skjermleserprogrammer, og ville ikke brukt noe slikt om han så hadde hatt det, siden han har på radioen på fullt volum fra han kommer hjem fra jobben til han legger seg.</p>
<p>Han snakker dessuten på telefonen mens han hører på radio og surfer på nettet. Han snakker med 100 personer i uken -- og aner ikke at det er uvanlig. Hvis du spilte «six degrees of separation» -- å finne seks mellomledd mellom deg og hvem som helst annen i verden -- ville Marius vært et av dine seks mellomledd. Han er en av dem som får ryktebørsen til å gå og som markedsførere tror de kan utnytte (men det kan de ikke). Han er grunnen til at telefonselskapet ditt ikke lenger tilbyr abonnement med ubegrensede lokalsamtaler. Marius elsker å fortelle vennene sine om kule nettsider han har sett. Han bruker ordet «sett» med vilje fordi han vet at det får dem til å føle seg ille til mote.</p>
