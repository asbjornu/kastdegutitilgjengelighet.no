<h2 class="entrysubject">Introduksjon</h2>
<p class="firstparagraph">Denne boka heter «Kast deg ut i tilgjengelighet: Et mer tilgjengelig nettsted på 30 dager», og vil gi deg svaret på to spørsmål. Det første spørsmålet er «Hvorfor bør jeg gjøre nettstedet mitt mer tilgjengelig?» Om du ikke har et nettsted, så er ikke denne boka for deg. Det andre spørsmålet er «Hvordan kan jeg gjøre nettstedet mitt mer tilgjengelig?» Er du ikke overbevist av svaret på det første spørsmålet, vil du nok ikke være interessert i det andre.</p>
<p>For å gi deg svaret på det første spørsmålet, vil du bli kjent med fem ulike personer: <a href="dag_1_anne">Anne</a>, <a href="dag_2_jorgen">Jørgen</a>, <a href="dag_3_knut">Knut</a>, <a href="dag_4_lillian">Lillian</a> og <a href="dag_5_marius">Marius</a>. Disse personene har flere ting til felles:</p>

<ol>
<li>De har alle en kombinasjon av fysiske, psykiske og teknologiske funksjonshemninger som gjør det vanskeligere for dem å bruke Internett.</li>
<li>Selv om de er fiktive, representerer de alle virkelige mennesker med funksjonshemninger, og de benytter Internett på samme måte som virkelige mennesker med funksjonshemninger ville gjort.</li>
<li>De har alle vanskeligheter med å lese ditt nettsted.</li>
</ol>

<p>For å svare på det andre spørsmålet, vil jeg gi deg 25 tips som du med en gang kan benytte på ditt eget nettsted for å gjøre det mer tilgjengelig. Selv om disse konseptene gjelder for alle nettsteder, vil jeg fokusere på hvordan du kan implementere disse i noen populære webloggverktøy. Hvis du benytter andre publiseringsverktøy, er du selv nødt til å finne ut hvordan du kan implementere disse tipsene i det verktøyet du benytter.</p>
<p>Hvert tips vil fokusere på et enkelt konsept, forklare bakgrunnen for det og vise deg hvem som drar fordeler av det når du implementerer det i nettstedet ditt. Dette er grunnen til at personskildringene kommer først, fordi det kan endre spørsmålsstillingen fra «Hvorfor skal jeg gidde?» til «Hvem har nytte av det?» Svar: «Marius har nytte av det.» «Hvordan har Marius nytte av dette?» «Vel, la oss se på det ...» og så videre.</p>
<p>Ikke få panikk om du ikke er en HTML-ekspert. Ikke få panikk om det eneste nettstedet du har er en personlig weblogg, der du har plukka ut en ferdig mal da du begynte å skrive og ikke har rørt malen siden. Jeg skal ikke fortelle deg at du må gjøre radikale endringer i utseendet på sidene dine, kaste ut alle de nøsta tabellene og konvertere til <acronym title="extensible hypertext markup language">XHTML</acronym> og <acronym title="cascading style sheets">CSS</acronym>. Dette handler om å ta det du har og gjøre det bedre med små, men viktige endringer. Anne, Jørgen, Knut, Lillian og Marius vil takke deg for oppmerksomheten.</p>
