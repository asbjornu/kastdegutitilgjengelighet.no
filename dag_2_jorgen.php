<h2 class="entrysubject">Dag 2: Jørgen</h2>
<p class="firstparagraph">Jørgen er en 27 år gammel pensjonist som bor sammen med kjæresten Christine i området Fevik nær Grimstad. Da Jørgen var 22 år gammel, startet han et lite firma som spesialiserte seg på å legge høyhastighets fiberkabler. To år senere solgte han firmaet i rett tid, til det samme verdensomspennende telekommunikasjonskonglomeratet som akkurat nå er på telefonen og prøver å forklare Jørgen hvorfor han ikke kan få noe bedre enn 56K oppringt internettilkopling i den nye leiligheten som koster 10 000 kroner i måneden. Det virker som alt kan kjøpes for penger -- bortsett fra kompetanse.</p>
<p>Jørgen ser verden på en litt annen måte enn du gjør. Dette er ikke noen forretningsklisjé eller et filosofisk utsagn; han ser faktisk verden på en annen måte enn du. Han er fargeblind og kan ikke skille rødt fra grønt (særlig rødfarger virker veldig mørke, nesten svarte). Alle klærne hans er diskret merket med bokstaver: R for rød, MG for mørkegrønn og lignende. Kjæresten hans har laget en kompatibilitetstabell som viser hvilke klær han får lov til å bruke sammen. Han følger instruksene til punkt og prikke, selv om han ikke skjønner betydningen av dem.</p>
<p>Mens kundebehandleren forteller Jørgen at han er evig fordømt, utfører Jørgen sine vanlige internettgjøremål. Nå som han er pensjonert bruker han mesteparten av tiden sin på å samle på kretskort til gamle spillemaskiner, reparere dem, sette dem inn i kabinetter, pusse dem opp og selge dem videre via nettauksjoner. Egentlig er det kjæresten hans som tar seg av oppussingen, men Jørgen gjør alt det andre. Han har dessuten en hunnilder som heter Rolf. Christine klaget over at hannildere lukter for mye, så de kom frem til et kompromiss. Rolf bruker mesteparten av tiden sin på å sove i det øverste venstre hjørnet i sofaen, men kommer ut og setter seg i vinduskarmen under morgenrushet for å se på trafikken. Faktisk gjør Jørgen det samme. Christine gjør det ikke, hun sover heller litt lenger. Det er et øyeblikk for en mann og hans ilder.</p>
<p>Som du sikkert forstår krever hobbyen til Jørgen ganske mye kommunikasjon via nettet med tidligere og potensielle kunder, leverandører, spillehalleiere og alle andre som kan ha tips om en obskur spillemaskindel. Han forbanner 56-men-egentlig-28-idag-K oppringt internettilgang og starter nettleseren sin. Noen dager bruker han den tekstbaserte nettleseren <a href="http://links.sourceforge.net/">Links</a>, som kan vise avanserte layouter i flere kolonner like bra som en grafisk nettleser, men kun med tekst -- ingen bilder eller skrifttyper. Andre dager bruker han <a href="http://www.opera.com/">Opera</a>, fordi den lar ham laste sider i bakgrunnen og gjør det enkelt å skru av og på visning av bilder. Vanligvis skrur han det av, for å spare båndbredde. I dag er det en Links-dag.</p>
<p>Trafikklys er røde, gule og grønne fra øverst til nederst. Hvis de står sidelengs blir det vrient -- og farlig. Jørgen har, i likhet med resten av de 8% av alle menn som er fargeblinde, lært å raskt få et overblikk over området rundt seg og følge stolpen fra bakken og opp til lysene. Lyset som -- langsmed stolpen -- er lengst fra bakken er rødt, det som er nærmest bakken er grønt. Det gule er alltid i midten.</p>
