<h2 class="entrysubject">Dag 22: Bruke ekte lister (eller etterlikne dem skikkelig)</h2>
<p class="firstparagraph">Anta at du har en leseliste med tre lenker: Slashdot, The Register, og dive into mark. Istedenfor en kjedelig sort kule, ønsker du et bilde med stilig utseende foran hver av dem. Hvordan gjør du det? Den vanligste løsningen er å bruke <code class="sgmltag">&lt;img&gt;</code>-tagger foran hver lenke. Det virker, og kan enkelt bli gjort mer tilgjengelig med å legge til en passende <code>alt</code>-tekst på hvert bilde.</p>
<p>Imidlertid kan du også gå videre og bruke ekte liste-oppmerking (<code class="sgmltag">&lt;ul&gt;</code> og <code class="sgmltag">&lt;li&gt;</code>-tagger), for deretter å bruke <acronym title="cascading style sheets">CSS</acronym> til å forandre den sorte kjedelige kulen til et bilde. I tillegg til å være «den riktige måten å gjøre det på» i en teoretisk forstand som du kan bry deg om eller ikke, har denne teknikken ytterligere tilgjengelighets-fordeler.</p>
<p>Jeg vil straks gi eksempler på begge teknikkene.</p>

<h3>Hvem har nytte av det?</h3>

<ol>
<li><a href="dag_5_marius">Marius</a> har nytte av det. <a href="dag_21_ignorere_avstandsbilder">Som vi så i går</a>, viser <a href="http://www.fdisk.com/doslynx/lynxport.htm">Lynx</a> filnavnet til ethvert bilde som ikke inneholder et <code>alt</code>-attributt.</li>

<li><p><a href="dag_2_jorgen">Jørgen</a> har nytte av det. <a href="http://links.sourceforge.net/">Links</a> viser aldri bilder, og som standard viser den <em>ingenting</em> for bilder som ikke har en <code>alt</code>-tekst. Dette var akseptabelt i går når vi ønsket å <a href="dag_21_ignorere_avstandsbilder">ignorere avstandsbilder</a>, men i dag ønsker vi å forsikre oss om at det er en indikasjon på at dette er en liste, så vi trenger den <code>alt</code>-teksten.</p>
<p>Også, når Jørgen surfer med bilder avslått, vil <a href="http://www.opera.com/">Opera</a> vise <code>alt</code>-teksten istedenfor en «manglende bilde»-boks. Og hvis du bruker den avanserte teknikken så vil Opera gjøre det enda bedre; den vender tilbake til å vise den normale sorte kulen istedenfor en «manglende bilde»-boks.</p>
</li>
<li><p><a href="dag_1_anne">Anne</a> har nytte av det. <a href="dag_21_ignorere_avstandsbilder">Som vi så i går</a>, leser <a href="http://www.freedomscientific.com/fs_products/software_jaws.asp">JAWS</a> filnavnet til ethvert bilde uten et <code>alt</code>-attributt. Lenkene ender opp med å forsvinne i et hav av irrelevante filnavn. Dette er det Anne hører:</p>

<blockquote>
<p>stilig kule dott gif lenke slashdot, stilig kule dott gif lenke the register, stilig kule dott gif lenke dive into mark</p>
</blockquote>

<p>Å tilby en asterisk (*) som <code>alt</code>-tekst hjelper veldig mye. JAWS vil se at bilde blir brukt som en kule, og meddeler det ikke. Imidlertid, <a href="http://www-3.ibm.com/able/hpr.html">Home Page Reader</a> vil fremdeles meddele asterisk-en uttrykkelig, så brukere vil høre dette:</p>

<blockquote>
<p>asterisk lenke slashdot, asterisk lenke the register, asterisk lenke dive into mark</p>
</blockquote>

<p>Å bruke ekte listeoppmerking er det beste. Ettersom all den visuelle presentasjonen er i <acronym title="cascading style sheets">CSS</acronym>-henvisningene, tilstopper ingenting av det siden, så både JAWS og Home Page Reader leser enkelt listen din for hva den er; en liste. Nå høres det slik ut:</p>

<blockquote>
<p>lenke slashdot, lenke the register, lenke dive into mark</p>
</blockquote>
</li>
</ol>

<h3>Hvordan gjøre det</h3>

<p>Hvis du har en leseliste som ser ut som dette:</p>

<blockquote>
<p><code class="sgmltag">&lt;img src="/images/stiligkule.gif" width="8" height="8"&gt; &lt;a href="http://www.slashdot.org/"&gt;Slashdot&lt;/a&gt; &lt;br&gt;<br />
&lt;img src="/images/stiligkule.gif" width="8" height="8"&gt; &lt;a href="http://www.theregister.co.uk/"&gt;The Register&lt;/a&gt; &lt;br&gt;<br />
&lt;img src="/images/stiligkule.gif" width="8" height="8"&gt; &lt;a href="http://diveintomark.org/"&gt;dive into mark&lt;/a&gt; &lt;br&gt;</code></p>
</blockquote>

<p>Burde du tilby <code>alt</code>-attributter for alle dine kule-bilder. Bruk en asterisk som <code>alt</code>-tekst, for å simulere det listen ville sett ut som hvis du hadde brukt ekte listeoppmerking. (For å forhindre dette fra å vises som et verktøytips i visuelle nettlesere, burde du også tilby et tomt <code>title</code>-attributt.)</p>

<blockquote>
<p><code class="sgmltag">&lt;img <strong>alt="*" title=""</strong> src="/images/stiligkule.gif" width="8" height="8"&gt; &lt;a href="http://www.slashdot.org/"&gt;Slashdot&lt;/a&gt; &lt;br&gt;<br />
&lt;img <strong>alt="*" title=""</strong> src="/images/stiligkule.gif" width="8" height="8"&gt; &lt;a href="http://www.theregister.co.uk/"&gt;The Register&lt;/a&gt; &lt;br&gt;<br />
&lt;img <strong>alt="*" title=""</strong> src="/images/stiligkule.gif" width="8" height="8"&gt; &lt;a href="http://diveintomark.org/"&gt;dive into mark&lt;/a&gt; &lt;br&gt;</code></p>
</blockquote>

<h3>Hvordan gjøre det: Avansert</h3>

<p>Den avanserte (og foretrukne) teknikken bruker <acronym title="cascading style sheets">CSS</acronym> til å definere at bildet skal bli brukt som en kule i listen.</p>

<blockquote>
<p><code class="sgmltag">&lt;style type="text/css"&gt;<br />
ul.leseliste {<br />
&nbsp;&nbsp;list-style: url(/images/stiligkule.gif) disc;<br />
}<br />
&lt;/style&gt;</code></p>
</blockquote>

<p>I malen din kan du så skrive en egen liste ved å bruke ekte listeoppmerking:</p>

<blockquote><p><code class="sgmltag">&lt;ul class="leseliste"&gt;<br />
&lt;li&gt;&lt;a href="http://www.slashdot.org/"&gt;Slashdot&lt;/a&gt;&lt;/li&gt;<br />
&lt;li&gt;&lt;a href="http://www.theregister.co.uk/"&gt;The Register&lt;/a&gt;&lt;/li&gt;<br />
&lt;li&gt;&lt;a href="http://diveintomark.org/"&gt;dive into mark&lt;/a&gt;&lt;/li&gt;<br />
&lt;/ul&gt;</code></p>
</blockquote>

<p>Resultat:</p>

<ul>
<li>Moderne nettlesere vil vise bilde som en kule-liste.</li>
<li>Nettlesere med bilder avslått vil vise den kjedelige sorte kulen.</li>
<li>Netscape 4 vil alltid vise den kjedelige sorte kulen.</li>
<li>Tekstbaserte nettlesere ignorerer alltid <acronym title="cascading style sheets">CSS</acronym>, så de vil vise listen slik som de normalt viser lister (gjengir vanligvis listens kule med en asterisk stjerne).</li>
</ul>

<h3>Etterskrift: ikke-kulede lister</h3>

<p>En annen vanlig måte å lage en liste med lenker er med å ikke bruke bilder i det hele tatt, bare en haug med lenker, muligens høyrestilt, som dette:</p>

<blockquote>
<p><code class="sgmltag">&lt;div align="hoyre"&gt;<br />
&lt;a href="http://www.slashdot.org/"&gt;Slashdot&lt;/a&gt;&lt;br&gt;<br />
&lt;a href="http://www.theregister.co.uk/"&gt;The Register&lt;/a&gt;&lt;br&gt;<br />
&lt;a href="http://diveintomark.org/"&gt;dive into mark&lt;/a&gt;&lt;br&gt;<br />
&lt;/div&gt;</code></p>
</blockquote>

<p>Dette kan også oppnås med <acronym title="cascading style sheets">CSS</acronym> og ekte listeoppmerking:</p>

<blockquote>
<p><code class="sgmltag">&lt;style type="text/css"&gt;<br />
ul.leseliste {<br />
&nbsp;&nbsp;list-style: none;<br />
&nbsp;&nbsp;text-align: right;<br />
}<br />
&lt;/style&gt;</code></p>
</blockquote>

<p>Eller, hvis du ønsker venstrestilte lenker, kan du gjøre dette i stedet:</p>

<blockquote>
<p><code class="sgmltag">&lt;style type="text/css"&gt;<br />
ul.leseliste {<br />
&nbsp;&nbsp;list-style: none;<br />
&nbsp;&nbsp;margin-left: 0;<br />
&nbsp;&nbsp;padding-left: 0;<br />
}<br />
&lt;/style&gt;</code></p>
</blockquote>

<p>Verken på den ene eller andre måten, så har ikke listeoppmerkingen forandret seg fra det forrige eksempelet:</p>

<blockquote><p><code class="sgmltag">&lt;ul class="leseliste"&gt;<br />
&lt;li&gt;&lt;a href="http://www.slashdot.org/"&gt;Slashdot&lt;/a&gt;&lt;/li&gt;<br />
&lt;li&gt;&lt;a href="http://www.theregister.co.uk/"&gt;The Register&lt;/a&gt;&lt;/li&gt;<br />
&lt;li&gt;&lt;a href="http://diveintomark.org/"&gt;dive into mark&lt;/a&gt;&lt;/li&gt;<br />
&lt;/ul&gt;</code></p>
</blockquote>

<p>«<code>list-style: none</code>»-linjen fjerner den vanlige sorte kulen i visuelle nettlesere. Dette fungerer i alle nettlesere, til og med i Netscape 4. Takk til <a href="http://www.royal-ts.de/">Tobias Schmidt</a> for at han minnet meg på denne teknikken.</p>

