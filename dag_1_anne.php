<h2 class="entrysubject">Dag 1: Anne</h2>
<p class="firstparagraph">Anne Merete, eller bare Anne, bor sammen med sin mor i Lillestrøm i Akershus. Hun er 19 år gammel og går 3. året ved den videregående skolen, der hun er en fremragende elev. Grunnen til de gode resultatene er dels at hun får ekstra undervisningstimer, men hovedsakelig at hun har gode studievaner og leser flittig. Hun har alle lesebøkene på lydbånd og disse lytter hun på i en spesiell kassettspiller som kan spille av båndene opptil tre ganger så raskt som normalt med minimale forstyrrelser. Hun har vært blind i 8 år.</p>
<p>Siden hun ikke er født blind, har Anne forståelse for visuelle begreper som for eksempel farger, og hun snakker fremdeles om farger med moren sin i forhold til ting hun kjente «før». Et emne hun ikke snakker om er bilulykken som drepte faren hennes og gjorde henne blind -- en episode som bare blir referert til indirekte gjennom preposisjonene «før» og «etter». «Denne er grønn som veggene i stua var før.» «Den er på en måte som den rosa genseren du brukte før, bare lysere» og så videre.</p>
<p>Faglig ligger Anne ett år etter klassekameratene på skolen på grunn av vanskelighetene hun hadde med å tilpasse seg sin nye livssituasjon rett etter ulykka. Til høsten skal hun ta grunnfag i sammenlignende litteratur ved Universitetet i Oslo. Hun gleder seg veldig til å begynne på universitetet, delvis fordi hun håper på å få seg nye venner og delvis fordi hun vil kunne gjøre mye mer av skolearbeidet sitt på Internett; lese timeplaner, levere skriftlige oppgaver og sende øyeblikksmeldinger til lærere og klassekamerater. Hun har ikke så veldig mange venner på den videregående skolen, hun er mye sammen med moren sin og er ellers på Internett.</p>
<p>Hun benytter over 2000 kroner i måneden på lydbøker, musikk og diverse tekniske dippedutter. De fleste av lydbøkene hennes er fremdeles på kassettbånd, men hun finner mer og mer interessant lesestoff som hun kan laste ned og la e-bokleseren lese for henne. Musikk på CD-er, dippedutter, dippedutter og flere dippedutter, alt kjøper hun i nettbutikker.</p>
<p>Å handle på nett er ikke alltid like enkelt, men det er likevel noe helt annet enn å ta toget til nærmeste kjøpesenter og prøve å få hjelp i en av butikkene der. Dessuten trenger hun ikke førerhunden Arthur når hun handler på Internett. Hun liker ikke Arthur, han er ikke like god som de andre førerhundene hun har, Lancelot og Guinevere. Begge disse er blitt pensjonister og bor saman med Anne og moren hennes. Hun skiller dem fra hverandre ved hjelp av halsbåndene deres. Arthur sitt halsbånd er glatt, Lancelot sitt har pigger og Guinevere sitt har riller i seg.</p>
<p>Som brorparten av blinde, kan ikke Anne så mye blindeskrift. Hun har en merkemaskin som lager etiketter med blindeskrift. Denne benytter hun for å merke CD-ene, men hun kan ikke lese bøker med blindeskrift, fordi de gjerne er skrevet i kortskrift, noe hun ikke har lært seg. Når hun handler eller spiller på verdensveven, benytter Anne den siste utgaven av <a href="http://www.freedomscientific.com/fs_products/software_jaws.asp">JAWS</a>, en skjermleser som kan brukes sammen med Internet Explorer på Windows. JAWS benyttar avansert tekst-til-tale-syntese for høytlesing av nettsider. Programmet har også en vanvittig samling av esoteriske hurtigtaster for at man skal kunne navigere seg gjennom verdensveven. Anne har lært seg alle tastekombinasjonene. Hun kan lese velutformede nettsider raskere enn hun leser lydbøkene sine.</p>
