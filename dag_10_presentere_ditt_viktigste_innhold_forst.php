<h2 class="entrysubject">Dag 10: Presentere ditt viktigste innhold først</h2>
<p class="firstparagraph">En av de viktigste fordelene med helt og holdent å bruke en <acronym title="cascading style sheets">CSS</acronym>-basert mal er at det er enkelt å omordne elementer i HTML-kildekoden uten å berøre den visuelle layouten, slik at ditt viktigste innhold vises mens resten av siden fortsatt lastes. Dog, jeg er klar over at de fleste nettsider fortsatt bruker tabellbaserte maler, så dette tipset er for deg.</p>

<p>Hvis du har en tabellbasert mal med en navigasjonsliste langs venstre side, blir navigasjonslisten presentert for blinde brukere som <a href="dag_5_marius">Marius</a> og <a href="dag_1_anne">Anne</a> før hovedinnholdet. Det er umulig å beskrive hvor stort problem dette er; du må se det selv:</p>

<ol>
<li><a href="http://diveintoaccessibility.org/examples/notabletrick.html">Eksempel på tabellbasert layout</a>.</li>
<li><a href="http://diveintoaccessibility.org/examples/tabletrick.html">Modifisert layout, med innholdet først</a>. Disse to malene burde se vesentlig like ut i visuelle nettlesere, men i Lynx er forskjellen åpenbar.</li>
<li><a href="http://diveintoaccessibility.org/examples/notabletrick.txt">Den originale layouten, gjengitt av Lynx</a>.</li>
<li><a href="http://diveintoaccessibility.org/examples/tabletrick.txt">Den modifiserte layouten, gjengitt av Lynx</a>. Det viktigste innholdet vises først, deretter navigasjonslisten.</li>
</ol>

<p>Du trenger ikke å redesigne malen din helt fra bunnen av for å unngå dette problemet. Det er en (relativt) enkel teknikk, i hengiven affekt kalt «tabelltrikset», som kan presentere ditt viktigste innhold først, mens du fortsatt beholder navigasjonslisten langs venstre side.</p>

<h3>Hvem har nytte av det?</h3>

<ol>
<li><a href="dag_5_marius">Marius</a> har nytte av det. Som demonstrert i eksempelet ovenfor, viser Lynx innhold i samme rekkefølge som innholdet har i HTML-kilden. Det betyr at Marius må skrolle gjennom hele navigasjonslisten hver eneste gang han besøker siden din. Skrolling suger.</li>
<li><a href="dag_1_anne">Anne</a> har nytte av det. <a href="http://www.freedomscientific.com/fs_products/software_jaws.asp">JAWS</a>, akkurat som Lynx, presenterer innhold i den rekkefølgen som det vises i HTML-kildekoden, ikke i den rekkefølgen som vises på skjermen. Med JAWS er problemet enda verre, fordi Anne må vente mens JAWS leser gjennom hele navigasjonslisten før hun kan høre noe virkelig innhold, og det finnes ingen sikker metode for å hoppe direkte til det viktigste innholdet. (Vi snakker mer om dette problemet på mandag).</li>
<li><a href="http://www.google.no/">Google</a> tjener på det. Google vektlegger innhold nær toppen av siden sterkest. Det vil si toppen av HTML-kildekoden, ikke den visuelle toppen av siden. Faktisk er de fleste som vet om denne teknikken i søkemotoroptimaliserings-bransjen; for dem er tilgjengelighetsfordelene underordnet.</li>
</ol>

<h3>Hvordan gjøre det</h3>

<p>Se på din egen side i <a href="http://www.delorie.com/web/lynxview.html">Lynx Viewer</a> og se om daglige innlegg vises først, før navigasjonen. Movable Types standardmal gjør dette riktig; hvis du bruker standardmalen eller noe basert på den, så trenger du sannsynligvis ikke å forandre på noe. Men se på siden din i Lynx Viewer allikevel, for det vil gi deg en dypere forståelse av temaet.</p>

<p>Hvis du bruker en av Radios standardmaler, må du kanskje justere tabellene til å sette det viktigste innholdet først. Det er ingen spesifikk kopier-og-lim-inn måte å gjøre dette på, du må grave deg inn i din egen mal og kikke på tabell-strukturen. <a href="http://diveintoaccessibility.org/examples/notabletrick.html">Eksempel-layouten</a> og den <a href="http://diveintoaccessibility.org/examples/tabletrick.html">modifiserte eksempel-layouten</a> viser deg den grunnleggende teknikken:</p>

<p>I stedet for den åpenbare tabell-layouten:</p>

<blockquote><pre><code>&lt;table&gt;
&lt;tr&gt;
  &lt;td valign="top" align="left" width="25%"&gt;
    ... navigasjonsliste ...
  &lt;/td&gt;
  &lt;td valign="top" align="left"&gt;
    ... viktigste innhold ...
  &lt;/td&gt;
&lt;/tr&gt;
&lt;/table&gt;</code></pre></blockquote>

<p>Så gjør vi dette i stedet:</p>

<blockquote><pre><code>&lt;table&gt;
&lt;tr&gt;
  &lt;!-- <a href="images/1.gif">mellomroms-GIF</a> i øverste venstre celle --&gt;
  &lt;td&gt;&lt;img src="/images/1.gif" width="1" height="1" alt=""&gt;&lt;/td&gt;
  &lt;!-- main content cell first, with rowspan=2 --&gt;
  &lt;td valign="top" align="left" rowspan="2"&gt;
    ... viktigste innhold  ...
  &lt;/td&gt;
&lt;/tr&gt;
&lt;tr&gt;
  &lt;td valign="top" align="left" width="25%"&gt;
    ... navigasjonsliste ...
  &lt;/td&gt;
&lt;/tr&gt;
&lt;/table&gt;</code></pre></blockquote>

